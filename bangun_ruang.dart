void main(List<String> args) {
  int alas = 8;
  int tinggi = 4;
  double hasil = alas * tinggi / 2;

  print("Hasil perhitungan luas segitiga dari: ");
  print("Alas $alas");
  print("Tinggi $tinggi");
  print("Hasilnya: $hasil");

  int jariJari = 8;
  final double phi = 3.14;
  // double phi = 22 / 7;
  double luasLingkaran = phi * (jariJari * jariJari);

  print("Hasil perhitungan luas lingkaran dari: ");
  print("Jari-jari $jariJari");
  print("Phi $phi");
  print("Hasilnya: $luasLingkaran");
}

